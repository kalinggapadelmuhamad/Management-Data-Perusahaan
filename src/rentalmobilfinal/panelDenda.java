    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String nama = namaDenda.getText();
        try {
            Connection con = new conn().conn();
            java.sql.Statement stat = con.createStatement();
            String sql = "SELECT * FROM denda WHERE namaDenda = '"+ nama +"'";
            ResultSet data = stat.executeQuery(sql);

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Denda");
            model.addColumn("Nama Denda");
            model.addColumn("Harga Denda");
            jTable1.setModel(model);

            if(data.next()){
                model.addRow(new Object[]{
                    data.getString("idDenda"),
                    data.getString("namaDenda"),
                    data.getString("hargaDenda"),
                });
                jTable1.setModel(model);

            }else{
                JOptionPane.showMessageDialog(null, "Data Tidak Ada","Perhatian",JOptionPane.WARNING_MESSAGE);
                tampilData();
            }

        }catch (SQLException ex) {
            Logger.getLogger(panelCs.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


