    private void tampilData(){
        //membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Denda");
        model.addColumn("Nama Denda");
        model.addColumn("Harga Denda");
        jTable1.setModel(model);
    
    //menampilkan data database kedalam tabel
    try {
        Connection con = new conn().conn();
        java.sql.Statement stat = con.createStatement();
        ResultSet data = stat.executeQuery("SELECT * FROM denda");
        
        while (data.next()) {
            model.addRow(new Object[]{
                data.getString("idDenda"),
                data.getString("namaDenda"),
                data.getString("hargaDenda"),
            });
            jTable1.setModel(model);
        }
    } catch (Exception e) {
        System.err.println("Terjadi Kesalahan :" + e);
      }
    }
